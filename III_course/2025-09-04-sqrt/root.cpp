#include <cmath>

// Зараз root є обгорткою над стандартною std::sqrt
// Приймає параметр типу double (будь-яке дійсне число, в т.ч. ірраціональні значення у поданні з плаваючою крапкою).
// Повертає корінь квадратний числа
double root(double x) {
    return std::sqrt(x);
}