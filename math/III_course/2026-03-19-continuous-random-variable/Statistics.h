#pragma once

#include <vector>
#include <cmath>
#include <stdexcept>

/**
 * Клас Statistics надає набір статичних методів для обчислення
 * основних числових характеристик випадкових величин.
 *
 * Підтримуються два типи задач:
 *
 * 1. Неперервні розподіли
 *    Обчислення виконується через чисельне інтегрування щільності
 *    розподілу f(x) на відрізку [a, b] методом Сімпсона.
 *
 *    Доступні характеристики:
 *    - математичне сподівання
 *    - другий початковий момент
 *    - дисперсія
 *    - середнє квадратичне відхилення
 *
 * 2. Емпіричні вибірки
 *    Обчислення виконується безпосередньо за даними вибірки
 *    як середні значення відповідних функцій.
 *
 *    Доступні характеристики:
 *    - вибіркове середнє
 *    - дисперсія
 *    - виправлена дисперсія
 *    - середнє квадратичне відхилення
 *    - виправлене середнє квадратичне відхилення
 *
 * Клас є утилітним:
 * - всі методи статичні
 * - не потребує створення об'єктів
 *
 * Узагальнення:
 * - для розподілів використовується template параметр Function
 * - для вибірок використовується template параметр Container
 *
 * Вимоги:
 * - Function має бути викликаним як f(x)
 * - Container має підтримувати empty(), size() та ітерацію
 *
 * Зауваження:
 * - для неперервних розподілів точність залежить від методу інтегрування
 * - для емпіричних даних точність визначається лише вибіркою
 */
class Statistics {
public:
    Statistics() = delete;
    ~Statistics() = delete;

    Statistics(const Statistics&) = delete;
    Statistics& operator=(const Statistics&) = delete;

public:
    /**
     * Обчислює математичне сподівання неперервної випадкової величини
     * за її щільністю розподілу на відрізку [a, b].
     *
     * Формула:
     * E[X] = ∫[a,b] x f(x) dx
     *
     * Параметри:
     * f  Функція щільності розподілу f(x).
     * a  Ліва межа інтегрування.
     * b  Права межа інтегрування.
     *
     * Повертає:
     * Наближене значення математичного сподівання.
     */
    template<typename Function>
    static double expectation(Function f, double a, double b);

    /**
     * Обчислює дисперсію неперервної випадкової величини
     * за її щільністю розподілу на відрізку [a, b].
     *
     * Формула:
     * D[X] = E[X^2] - (E[X])^2
     *
     * Параметри:
     * f  Функція щільності розподілу f(x).
     * a  Ліва межа інтегрування.
     * b  Права межа інтегрування.
     *
     * Повертає:
     * Наближене значення дисперсії.
     */
    template<typename Function>
    static double variance(Function f, double a, double b);

    /**
     * Обчислює середнє квадратичне відхилення неперервної випадкової величини
     * за її щільністю розподілу на відрізку [a, b].
     *
     * Формула:
     * σ = sqrt(D[X])
     *
     * Параметри:
     * f  Функція щільності розподілу f(x).
     * a  Ліва межа інтегрування.
     * b  Права межа інтегрування.
     *
     * Повертає:
     * Наближене значення середнього квадратичного відхилення.
     */
    template<typename Function>
    static double standard_deviation(Function f, double a, double b);

    /**
     * Обчислює вибіркове середнє для емпіричної вибірки
     * як оцінку математичного сподівання випадкової величини.
     *
     * Формула:
     * x̄ = (1 / n) * Σ x_i
     *
     * Параметри:
     * data  Вектор значень емпіричної вибірки.
     *
     * Повертає:
     * Значення вибіркового середнього.
     *
     * Винятки:
     * std::invalid_argument, якщо вибірка порожня.
     */
    template<typename Container>
    static double expectation(const Container& data);

    /**
     * Обчислює емпіричну дисперсію вибірки.
     *
     * Формула:
     * s^2 = E_emp[X^2] - (x̄)^2
     *
     * Еквівалентно:
     * s^2 = (1 / n) * Σ (x_i - x̄)^2
     *
     * Параметри:
     * data  Вектор значень емпіричної вибірки.
     *
     * Повертає:
     * Значення емпіричної дисперсії.
     *
     * Винятки:
     * std::invalid_argument, якщо вибірка порожня.
     */
    template<typename Container>
    static double variance(const Container& data);

    /**
    * Обчислює емпіричну дисперсію вибірки за стабільною формулою.
    *
    * Формула:
    * s^2 = (1 / n) * Σ (x_i - x̄)^2
    *
    * На відміну від формули:
    * s^2 = E[X^2] - (x̄)^2
    * ця реалізація є більш чисельно стабільною при великих значеннях.
    *
    * Параметри:
    * data  Контейнер значень емпіричної вибірки.
    *
    * Повертає:
    * Значення емпіричної дисперсії.
    *
    * Винятки:
    * std::invalid_argument, якщо вибірка порожня.
    */
    template<typename Container>
    static double variance_stable(const Container& data);

    /**
     * Обчислює виправлену дисперсію емпіричної вибірки.
     *
     * Формула:
     * s^2 = (1 / (n - 1)) * Σ (x_i - x̄)^2
     *
     * Параметри:
     * data  Вектор значень емпіричної вибірки.
     *
     * Повертає:
     * Значення виправленої дисперсії.
     *
     * Винятки:
     * std::invalid_argument, якщо кількість елементів вибірки менша за 2.
     */
    template<typename Container>
    static double variance_unbiased(const Container& data);

    /**
     * Обчислює емпіричне середнє квадратичне відхилення.
     *
     * Формула:
     * s = sqrt(s^2)
     *
     * Параметри:
     * data  Вектор значень емпіричної вибірки.
     *
     * Повертає:
     * Значення емпіричного середнього квадратичного відхилення.
     *
     * Винятки:
     * std::invalid_argument, якщо вибірка порожня.
     */
    template<typename Container>
    static double standard_deviation(const Container& data);

    /**
     * Обчислює виправлене середнє квадратичне відхилення.
     *
     * Формула:
     * s = sqrt(s^2)
     *
     * де s^2 це виправлена дисперсія.
     *
     * Параметри:
     * data  Вектор значень емпіричної вибірки.
     *
     * Повертає:
     * Значення виправленого середнього квадратичного відхилення.
     *
     * Винятки:
     * std::invalid_argument, якщо кількість елементів вибірки менша за 2.
     */
    template<typename Container>
    static double standard_deviation_unbiased(const Container& data);

private:
    /**
     * Чисельно обчислює визначений інтеграл функції на відрізку [a, b]
     * методом Сімпсона.
     *
     * Формула:
     * ∫[a,b] f(x) dx
     *
     * Параметри:
     * f  Підінтегральна функція.
     * a  Ліва межа інтегрування.
     * b  Права межа інтегрування.
     *
     * Повертає:
     * Наближене значення визначеного інтеграла.
     */
    template<typename Function>
    static double integrate(Function f, double a, double b);

    /**
     * Обчислює другий початковий момент неперервної випадкової величини
     * за її щільністю розподілу на відрізку [a, b].
     *
     * Формула:
     * E[X^2] = ∫[a,b] x^2 f(x) dx
     *
     * Параметри:
     * f  Функція щільності розподілу f(x).
     * a  Ліва межа інтегрування.
     * b  Права межа інтегрування.
     *
     * Повертає:
     * Наближене значення другого початкового моменту.
     */
    template<typename Function>
    static double second_moment(Function f, double a, double b);

    /**
     * Обчислює середнє значення квадратів елементів емпіричної вибірки.
     *
     * Формула:
     * E_emp[X^2] = (1 / n) * Σ x_i^2
     *
     * Параметри:
     * data  Вектор значень емпіричної вибірки.
     *
     * Повертає:
     * Значення середнього квадрата.
     *
     * Винятки:
     * std::invalid_argument, якщо вибірка порожня.
     */
    template<typename Container>
    static double second_moment(const Container& data);
};

#include "Statistics.tpp"
