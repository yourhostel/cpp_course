/*
    Завдання 1:
    Ввести з клавіатури три цілі числа і вивести на екран значення їх суми.
*/

#include <iostream> // Підключаємо бібліотеку для введення-виведення
#include "console_utils.h" // Підключаємо наші функції кодування

using namespace std;

int main() {
    int num1, num2, num3; // Оголошуємо три змінні для збереження введених чисел
    int sum; // Змінна для збереження суми чисел

    // Встановлюємо кодування для кирилиці
    setCyrillicEncoding();

    // Просимо користувача ввести три цілі числа
    cout << "Введіть перше число: ";
    cin >> num1; // Зчитуємо перше число

    cout << "Введіть друге число: ";
    cin >> num2; // Зчитуємо друге число

    cout << "Введіть третє число: ";
    cin >> num3; // Зчитуємо третє число

    // Обчислюємо суму трьох чисел
    sum = num1 + num2 + num3;

    // Виводимо на екран суму чисел
    cout << "Сума чисел: " << sum << endl;

    setUtf8Encoding();

    return 0; // Повертаємо 0 для позначення успішного завершення програми
}
