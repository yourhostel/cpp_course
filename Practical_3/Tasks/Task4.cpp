/*
    Завдання 4:
    Написати програму обчислення радіусів R описаного і r вписаного в квадрат
    кіл за його стороною a.
*/

#include <iostream>
#include <cmath> // Для використання функції sqrt
#include "console_utils.h" // Підключаємо наші функції кодування

using namespace std;

// Функція для обчислення радіусів R (описаного) і r (вписаного) в квадрат
void processTask4() {
    // Встановлюємо кодування для кирилиці
    setCyrillicEncoding();

    double a;  // Сторона квадрата
    double r;  // Радіус вписаного кола
    double R;  // Радіус описаного кола

    // Введення сторони квадрата
    cout << "Введіть сторону квадрата (a): ";
    cin >> a;

    // Обчислення радіусів
    r = a / 2;              // Радіус вписаного кола
    R = a / sqrt(2);        // Радіус описаного кола

    // Виведення результатів
    cout << "Радіус вписаного кола (r): " << r << endl;
    cout << "Радіус описаного кола (R): " << R << endl;

    // Повертаємо кодування до UTF-8
    setUtf8Encoding();
}

int main() {
    // Викликаємо функцію обробки
    processTask4();

    return 0;
}
